/// ---------------------------------------------------------------------------
/// 模式：单调栈（Monotonic Stack）
/// ---------------------------------------------------------------------------
///
/// ## 核心原理
/// 单调栈是一种特殊的栈，其内部元素始终保持单调递增或单调递减的顺序。
/// 当新元素入栈时，如果不满足单调性，就不断弹出栈顶元素直到满足条件。
/// 这使得单调栈能高效地回答"下一个更大/更小元素"这类问题。
///
/// 关键性质：
/// - 单调递增栈：栈底到栈顶递增。用于找左右两侧第一个更小的元素。
/// - 单调递减栈：栈底到栈顶递减。用于找左右两侧第一个更大的元素。
///
/// ## 适用场景
/// - 寻找下一个更大元素（Next Greater Element）。
/// - 寻找下一个更小元素（Next Smaller Element）。
/// - 直方图中最大矩形面积。
/// - 接雨水问题。
/// - 股票价格跨度。
///
/// ## 时间复杂度
/// - O(n)，每个元素最多入栈一次、出栈一次。
///
/// ## 空间复杂度
/// - O(n)，栈中最多存储 n 个元素。

// ============================================================================
// LeetCode #496: Next Greater Element I
// ============================================================================
// 所属模式：Monotonic Stack（单调栈）
// 难度：Easy
// 题目描述：
//   给定两个没有重复元素的数组 nums1 和 nums2，nums1 是 nums2 的子集。
//   找出 nums1 中每个元素在 nums2 中的下一个更大元素。
//   nums2 中元素 x 的下一个更大元素是指 x 在 nums2 中右侧第一个比 x 大的元素。
//   若不存在，返回 -1。
//
// 核心考点：
//   - 单调递减栈的基本使用。
//   - 用 HashMap 存储每个元素的下一个更大元素。
//   - 单调栈的模板式实现。
//
// 解题思路推导：
//   遍历 nums2，维护一个单调递减栈（栈底大、栈顶小）。
//   当遇到一个比栈顶大的元素时，说明这个元素就是栈顶元素的"下一个更大元素"。
//   弹出栈顶，记录结果，继续检查新的栈顶。
//   最后栈中剩余的元素没有下一个更大元素，结果为 -1。
//
// 实现步骤：
//   1. 创建 HashMap 用于存储每个元素的下一个更大元素。
//   2. 创建栈。
//   3. 遍历 nums2 中每个元素 num：
//      a. while 栈非空 且 num > 栈顶元素：
//         - 弹出栈顶元素 top。
//         - map[top] = num（num 是 top 的下一个更大元素）。
//      b. num 入栈。
//   4. 栈中剩余元素的下一个更大元素均为 -1。
//   5. 遍历 nums1，从 map 中取结果（不存在则返回 -1）。

List<int> nextGreaterElement(List<int> nums1, List<int> nums2) {
  final Map<int, int> map = {}; // 存储每个元素的下一个更大元素
  final List<int> stack = []; // 单调递减栈

  // 遍历 nums2，构建映射关系
  for (int num in nums2) {
    // 维护单调递减栈：栈顶元素小于当前元素 → 当前元素即是栈顶的"下一个更大元素"
    while (stack.isNotEmpty && num > stack.last) {
      int top = stack.removeLast(); // 弹出栈顶
      map[top] = num; // 记录该元素的下一个更大元素
    }
    stack.add(num); // 当前元素入栈
  }

  // 栈中剩余的元素没有下一个更大元素，默认返回 -1
  // 遍历 nums1，获取结果
  List<int> result = [];
  for (int num in nums1) {
    result.add(map[num] ?? -1); // 有记录则返回，否则 -1
  }
  return result;
}

// ============================================================================
// LeetCode #739: Daily Temperatures
// ============================================================================
// 所属模式：Monotonic Stack（单调栈 - 存储下标）
// 难度：Medium
// 题目描述：
//   给定一个整数数组 temperatures，表示每天的温度。返回一个数组 answer，
//   其中 answer[i] 是指对于第 i 天，需要等待多少天后才会有更高的温度。
//   如果之后都不会升温，则 answer[i] = 0。
//
// 核心考点：
//   - 单调栈存储下标而非值。
//   - 计算天数差。
//   - 与 #496 的区别：需要等待的天数 = 当前下标 - 栈顶下标。
//
// 解题思路推导：
//   与 #496 类似，但栈中存储的是下标（而不是温度值），因为我们需要计算天数差。
//   遍历 temperatures，维护单调递减栈（存储温度递减的下标）。
//   当遇到比栈顶下标对应的温度更高的温度时，弹出栈顶下标 idx，
//   则对于 idx 这一天，等待的天数 = 当前下标 i - idx。
//
//   举例：temperatures = [73,74,75,71,69,72,76,73]
//   i=0(73): 栈空，入栈 [0]
//   i=1(74): 74>73，弹出 0，answer[0]=1-0=1，入栈 [1]
//   i=2(75): 75>74，弹出 1，answer[1]=2-1=1，入栈 [2]
//   i=3(71): 71<75，可直接入栈 [2,3]
//   i=4(69): 69<71，入栈 [2,3,4]
//   i=5(72): 72>69(弹出4,answer[4]=1)，72>71(弹出3,answer[3]=2)，72<75(停)，入栈 [2,5]
//   ...最终 answer = [1,1,4,2,1,1,0,0]
//
// 实现步骤：
//   1. 初始化 answer 数组（全部为 0）。
//   2. 初始化栈（存储下标）。
//   3. 遍历 i 从 0 到 n-1：
//      a. while 栈非空 且 temperatures[i] > temperatures[栈顶]：
//         - idx = 弹出栈顶。
//         - answer[idx] = i - idx（等待天数）。
//      b. i 入栈。
//   4. 返回 answer（栈中剩余元素默认就是 0，不用处理）。

List<int> dailyTemperatures(List<int> temperatures) {
  int n = temperatures.length;
  List<int> answer = List<int>.filled(n, 0); // 初始化为 0
  List<int> stack = []; // 单调递减栈（存下标）

  for (int i = 0; i < n; i++) {
    // 当前温度高于栈顶下标对应的温度时，找到升温日
    while (stack.isNotEmpty && temperatures[i] > temperatures[stack.last]) {
      int idx = stack.removeLast(); // 弹出栈顶下标
      answer[idx] = i - idx; // 计算等待天数：当前下标 - 之前下标
    }
    stack.add(i); // 当前下标入栈
  }

  // 栈中剩余的元素保持默认值 0（后面没有更高温度）
  return answer;
}

