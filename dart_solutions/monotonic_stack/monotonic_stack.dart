// LeetCode #84: Largest Rectangle in Histogram
// ============================================================================
// 所属模式：Monotonic Stack（单调栈 - 找左右边界）
// 难度：Hard
// 题目描述：
//   给定 n 个非负整数，表示柱状图中各个柱子的高度。每个柱子宽度为 1。
//   求在该柱状图中的能够勾勒出的矩形的最大面积。
//
// 核心考点：
//   - 单调递增栈找到每个柱子的左右"第一个更小"边界。
//   - 以每个柱子为高的最大矩形面积 = 高度 × (右边界 - 左边界)。
//   - 哨兵（sentinel）技巧简化边界处理。
//
// 解题思路推导：
//   对于每根柱子 i，以它的高度为矩形高度的最大矩形，其左边界是左侧第一根更矮的柱子，
//   右边界是右侧第一根更矮的柱子。
//   使用单调递增栈（栈底到栈顶递增）：
//   - 当遇到比栈顶矮的柱子时，栈顶柱子的右边界就是当前柱子。
//   - 弹出栈顶后，新的栈顶就是左边界。
//   - 面积 = heights[弹出] × (右边界 - 左边界 - 1)。
//
//   举例：heights = [2,1,5,6,2,3]
//   i=0(2): 入栈 [0]
//   i=1(1): 1<2，弹出 0：面积=2×(1-(-1)-1)=2×1=2；入栈 [1]
//   i=2(5): 5>1，入栈 [1,2]
//   i=3(6): 6>5，入栈 [1,2,3]
//   i=4(2): 2<6，弹出 3：面积=6×(4-2-1)=6×1=6
//           2<5，弹出 2：面积=5×(4-1-1)=5×2=10；入栈 [1,4]
//   i=5(3): 3>2，入栈 [1,4,5]
//   遍历完，处理栈中剩余元素...返回最大面积 = 10
//
// 实现步骤：
//   1. 创建数组 heightsCopy，两端加入高度 0 的哨兵柱（简化边界处理）。
//   2. 初始化栈和 maxArea = 0。
//   3. 遍历每个柱子 i：
//      a. while 栈非空 且 heightsCopy[i] < heightsCopy[栈顶]：
//         - h = heightsCopy[弹出栈顶]。
//         - w = i - 栈顶 - 1（右边界 i，左边界新栈顶）。
//         - area = h × w；更新 maxArea。
//      b. i 入栈。
//   4. 返回 maxArea。

int largestRectangleArea(List<int> heights) {
  // 在原数组两端添加高度为 0 的哨兵
  List<int> h = [0, ...heights, 0];
  List<int> stack = [];
  int maxArea = 0;

  for (int i = 0; i < h.length; i++) {
    // 遇到比栈顶矮的柱子 → 栈顶柱子的右边界就是 i
    while (stack.isNotEmpty && h[i] < h[stack.last]) {
      int height = h[stack.removeLast()]; // 弹出栈顶的高度
      // 左边界是弹出后新的栈顶，右边界是 i
      int width = i - stack.last - 1;
      int area = height * width;
      if (area > maxArea) {
        maxArea = area; // 更新最大面积
      }
    }
    stack.add(i);
  }

  return maxArea;
}
