/// ---------------------------------------------------------------------------
/// 模式：单调栈（Monotonic Stack）
/// ---------------------------------------------------------------------------
///
/// ## 核心原理
/// 单调栈是一种特殊的栈，其内部元素始终保持单调递增或递减的顺序。
/// 新元素入栈时，若不满足单调性，就不断弹栈直到恢复单调性。
/// 这一特性使得单调栈能高效回答"下一个更大 / 更小元素"类问题。
///
/// 关键性质：
/// - 单调递减栈（栈底大 → 栈顶小）：用于找右侧第一个更大的元素。
/// - 单调递增栈（栈底小 → 栈顶大）：用于找右侧第一个更小的元素。
///
/// ## 适用场景
/// - 寻找下一个更大元素（Next Greater Element）。
/// - 每日温度（等待升温天数）。
/// - 直方图中最大矩形面积。
/// - 接雨水问题。
///
/// ## 时间复杂度
/// - O(n)，每个元素最多入栈一次、出栈一次。
///
/// ## 空间复杂度
/// - O(n)，栈中最多存储 n 个元素。

